// Table elements supports the following properties

let t = document.body.firstElementChild.firstElementChild;  // t will store table
console.log(t);

console.log(t.rows); //will return all the rows of the table

console.log(t.caption); //will return the caption of the table

console.log(t.tHead); // will return the table header
console.log(t.tFoot); // will return null

